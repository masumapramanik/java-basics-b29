package masuma;

public class DataTypes {
    public static void main(String[] args){

        //syntex:variable types,variable name=variable value
        int number1 = 100;
        float number2 = 12.223f;
        double number3 = 123476689;
        char letter = 'w';
        boolean bool = true;
        System.out.println("Int Number:" +number1);
        System.out.println("Float number2:" +number2);
        System.out.println("Double number3:" +number3);
        System.out.println("Char letter:" +letter);
        System.out.println("Boolean bool:" +bool);

        System.out.println("Int Number:"+number1+", Float number2: "+number2+",Double number3: "+number3);
        System.out.println("Int Number:"+number1+",\n Float number2: "+number2+",Double number3: "+number3);

        //Non primitive
        String name = "Masuma";
        int number[] = {1,2,3,4};
        System.out.println("Name:" +name);
        System.out.println("Numbers:"+number[2]);





    }



}
